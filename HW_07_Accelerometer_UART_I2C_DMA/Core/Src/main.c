/*
 After the START condition (ST) a slave address is sent, once a slave
 acknowledge (SAK) has been returned, an 8-bit sub-address (SUB) is transmitted: the 7
 LSB represent the actual register address while the MSB enables address auto increment. If
 the MSB of the SUB field is ‘1’, the SUB (register address) is automatically increased to
 allow multiple data read/write.

 In order to read multiple bytes, it is necessary to assert the most significant bit of the subaddress
 field. In other words, SUB(7) must be equal to 1 while SUB(6-0) represents the
 address of the first register to be read*/

//Addresses configuration
uint8_t accelerometer_lis2de = 0b01010000;
uint8_t accelerometer_lis2de12 = 0b00110000;
uint8_t control_reg1[] = { 0b00100000, 0b00011111 };
uint8_t control_reg2[] = { 0b00100001, 0b00000000 };
uint8_t control_reg4[] = { 0b00100011, 0b00000000 };

uint8_t out_x = 0x29, out_y = 0x2B, out_z = 0x2D;
uint8_t accelerometer;

//will enable the successive readings
uint8_t multiple_readings;

//since the position of the output register are distant 2 position we will have to allocate two
//more byte to arrive from out_x to out_z, to see positions read pag 27 of the datasheet
//NB: in GPIO impose data width of the dma request to byte
int8_t accelerations[6];

char buffer[100];
int len;

