void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {

	//the MSB becomes 1, this enable the autoincrement function
	multiple_readings = out_x + 128;

	//transmit the first register to read with MSB=1
	HAL_I2C_Master_Transmit(&hi2c1, accelerometer, &multiple_readings, 1, 100);

	//receive imposing the array to be written into
	HAL_I2C_Master_Receive_DMA(&hi2c1, accelerometer + 1,
			(uint8_t*) accelerations, 6);

}

void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c) {
	float acc_X = accelerations[0] / 64.0;
	float acc_Y = accelerations[2] / 64.0;
	float acc_Z = accelerations[4] / 64.0;

	len = snprintf(buffer, sizeof(buffer),
			"X= %+.3f g \r\nY= %+.3f g \r\nZ= %+.3f g \r\n", acc_X, acc_Y,
			acc_Z);

	HAL_UART_Transmit_DMA(&huart2, (uint8_t*) buffer, len);
}

